def bubble_sort (course, score):
    for i in range (0, len(score)-1):
        for j in range(0, len(score)-i-1):
            if(score[j]< score[j+1]):
                temp=score[j]
                score[j]=score[j+1]
                score[j+1]=temp
                temp=course[j]
                course[j]=course[j+1]
                course[j+1]=temp
    return course,score
    
